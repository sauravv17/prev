package com.teksystems;

public class switch_tut {
	public static void main(String[] args) {
		int switchval=5;
		switch(switchval) {
		case 1:
			System.out.println("Value 1");
			break;
		case 2:
			System.out.println("Value 2");
			break;
		case 3:
			System.out.println("Value 3");
			break;
		default:
			System.out.println("other");
			
		}
	}

}
